//Question 10 What is the difference between null and undefined in JavaScript? Show with examples.

//Answer
//Undefined
//undefined means a variable has been declared but has not been assigned a value.
//null
//null is an explicit value assigned to a variable to indicate "no value" or "empty."

//Example
var a = 5;
var b = null;
var c = undefined;

console.log(a); // 5
console.log(b); // null
console.log(c); // undefined